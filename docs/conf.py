def python_return_field() -> None:
    """Allow typed return fields in the Python domain.

    Replaces the default return-value field with a grouped field so that
    typed ``:return:`` and ``:returns:`` fields are parsed inline.
    """
    for index, field in enumerate(PyObject.doc_field_types):
        if field.name == "returnvalue":
            PyObject.doc_field_types[index] = PyGroupedField(
                field.name,
                label=field.label,
                names=field.names,
                rolename="class",
                can_collapse=True,
            )
            break


def python_yield_field() -> None:
    """Allow typed yield fields in the Python domain.

    Adds a new grouped field for typed ``:yield:`` and `:yields:`` directives.
    """
    PyObject.doc_field_types.append(
        PyGroupedField(
            "yieldvalue",
            label=_("Yields"),
            names=("yields", "yield"),
            rolename="class",
            can_collapse=True,
        ),
    )


