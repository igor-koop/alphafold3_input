# SPHINX HOOKS


def setup(app: Sphinx) -> None:
    """Register custom Sphinx hooks and Napoleon patches.

    Args:
        app (Sphinx): Sphinx application instance.
    """
    python_return_field()
    python_yield_field()
    setattr(
        GoogleDocstring,
        "_consume_fields",
        napoleon_fields_consumer,
    )
    setattr(
        GoogleDocstring,
        "_parse_attributes_section",
        napoleon_attributes_parser,
    )
    setattr(
        GoogleDocstring,
        "_consume_returns_section",
        napoleon_return_consumer,
    )
    setattr(
        GoogleDocstring,
        "_parse_yields_section",
        napoleon_yields_parser,
    )
    setattr(
        GoogleDocstring,
        "_parse_returns_section",
        napoleon_return_parser,
    )
    app.connect(
        event="autodoc-skip-member",
        callback=autodoc_skip_pydantic,
        priority=500,
    )
