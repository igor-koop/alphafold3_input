def autodoc_skip_pydantic(
    app: Sphinx,
    what: Literal[
        "module",
        "class",
        "exception",
        "function",
        "decorator",
        "method",
        "property",
        "attribute",
        "data",
        "type",
    ],
    name: str,
    obj: object,
    skip: bool,
    options: dict[str, str],
) -> bool | None:
    """Skip class-body fields represented by the ``INSTANCE_ATTR`` sentinel.

    Sphinx uses ``INSTANCE_ATTR`` for documented instance attributes (such as
    Pydantic model fields) that do not exist as ordinary runtime objects. This
    hook filters out such members when they are encountered under a class.

    Args:
        app (Sphinx): Sphinx application instance.
        what (Literal["module", "class", "exception", "function",
            "decorator", "method", "property", "attribute", "data",
            "type"]): Type of the parent object being documented.
        name (str): Member name passed by autodoc.
        obj (object): Member object or autodoc sentinel.
        skip (bool): Default autodoc skip decision.
        options (dict[str, str]): Autodoc directive options.

    Returns:
        bool | None: ``True`` if the member should be skipped, otherwise
        ``None`` to keep autodoc's default behavior.
    """
    del app, name, skip, options

    if what == "class" and obj == INSTANCE_ATTR:
        return True

    return None


