def napoleon_type_resolve(
    self: GoogleDocstring,
    typehint: str,
) -> str:
    """Resolve a Napoleon type hint.

    Rewrites ``Self`` to the owning class name and expands configured
    Napoleon type aliases.

    Args:
        typehint (str): Raw type hint extracted from a docstring section.

    Returns:
        str: Resolved type hint string.
    """
    resolved: str = typehint

    config: Config = getattr(self, "_config", Config())
    element: Any = getattr(self, "_obj", None)

    if "Self" in resolved and element is not None:
        if isinstance(element, type):
            resolved = resolved.replace("Self", element.__name__)
        else:
            resolved = resolved.replace(
                "Self",
                element.__qualname__.rsplit(".", 1)[0],
            )

    if aliases := config.napoleon_type_aliases:
        for alias, reference in aliases.items():
            if alias in resolved:
                resolved: str = resolved.replace(alias, reference)

    return resolved


def napoleon_fields_consumer(
    self: GoogleDocstring,
    parse_type: bool = True,
    prefer_type: bool = False,
    multiple: bool = False,
) -> list[tuple[str, str, list[str]]]:
    """Consume a field-style Napoleon section.

    This wraps the default field consumer and applies shared type-hint
    resolution to each parsed field.

    Args:
        parse_type (bool): Whether to parse an inline type from the field
            header.
        prefer_type (bool): Whether to treat an untyped field name as a type.
        multiple (bool): Whether comma-separated field names should be split
            into multiple entries.

    Returns:
        list[tuple[str, str, list[str]]]: Parsed field entries as
        ``(name, typehint, description)`` tuples.
    """
    self._consume_empty()

    fields: list[tuple[str, str, list[str]]] = []

    while not self._is_section_break():
        name, typehint, description = self._consume_field(
            parse_type,
            prefer_type,
        )

        if not any((name, typehint, description)):
            continue

        typehint: str = napoleon_type_resolve(self, typehint)

        if multiple and name:
            fields.extend(
                (item.strip(), typehint, description)
                for item in name.split(",")
            )
        else:
            fields.append((name, typehint, description))

    return fields


def napoleon_return_consumer(
    self: GoogleDocstring,
    preprocess_types: bool = False,
) -> list[tuple[str, str, list[str]]]:
    """Consume a Google-style ``Returns`` section.

    Parses the section into a single return entry and applies shared
    type-hint resolution to the parsed return type.

    Args:
        preprocess_types (bool): Unused compatibility parameter retained to
            match Napoleon's original method signature.

    Returns:
        list[tuple[str, str, list[str]]]: Parsed return entries as
        ``(name, typehint, description)`` tuples.
    """
    del preprocess_types

    lines: list[str] = self._dedent(self._consume_to_next_section())
    config: Config = getattr(self, "_config", Config())

    if not lines:
        return []

    name: str = ""
    typehint: str = ""
    description: list[str] = lines[1:]

    prefix, colon, suffix = self._partition_field_on_colon(lines[0])

    if colon:
        typehint: str = prefix
        description: list[str] = [suffix, *description]

    typehint: str = napoleon_type_resolve(self, typehint)
    description: list[str] = self.__class__(description, config).lines()

    return [(name, typehint, description)]


def napoleon_attributes_parser(
    self: GoogleDocstring,
    section: str,
) -> list[str]:
    """Parse an ``Attributes`` section.

    Follows the default Google-style attribute parser, while preserving
    ``ivar`` behavior and rendering attribute types through the shared
    type-resolution path.

    Args:
        section (str): Section label currently being parsed.

    Returns:
        list[str]: Lines of reStructuredText representing the parsed
        attributes.
    """
    del section

    lines: list[str] = []
    config: Config = getattr(self, "_config", Config())

    for name, typehint, description in self._consume_fields():
        resolved: str = typehint or napoleon_type_resolve(
            self,
            self._lookup_annotation(name),
        )

        if config.napoleon_use_ivar:
            fields: list[str] = self._format_block(
                f":ivar {name}: ",
                description,
            )
            lines.extend(fields)
            if resolved:
                lines.append(f":vartype {name}: {resolved}")
        else:
            lines.append(f".. py:attribute:: {name}")
            if resolved:
                lines.append(f"   :type: {resolved}")
            if self._no_index:
                lines.append("   :no-index:")
            lines.append("")

            fields = self._format_field("", "", description)
            lines.extend(self._indent(fields, 3))
            lines.append("")

    if config.napoleon_use_ivar:
        lines.append("")

    return lines


def napoleon_return_parser(
    self: GoogleDocstring,
    section: str,
) -> list[str]:
    """Parse a ``Returns`` section.

    Follows the default Google-style return parser, but renders resolved
    return types inline using typed ``:returns:`` fields.

    Args:
        section (str): Section label currently being parsed.

    Returns:
        list[str]: Lines of reStructuredText representing the parsed
        returns.
    """
    del section

    lines: list[str] = []
    config: Config = getattr(self, "_config", Config())

    for _name, typehint, description in self._consume_returns_section():
        if config.napoleon_use_rtype and typehint:
            lines.extend(
                self._format_block(
                    f":returns {typehint}: ",
                    description,
                ),
            )
        else:
            lines.extend(
                self._format_block(
                    ":returns: ",
                    description,
                ),
            )

    if lines and lines[-1]:
        lines.append("")

    return lines


def napoleon_yields_parser(
    self: GoogleDocstring,
    section: str,
) -> list[str]:
    """Parse a ``Yields`` section.

    Reuses the return parser but emits ``:yields:`` instead of ``:returns:``.
    """
    del section

    lines: list[str] = []
    config: Config = getattr(self, "_config", Config())

    for _name, typehint, description in self._consume_returns_section():
        if config.napoleon_use_rtype and typehint:
            lines.extend(
                self._format_block(
                    f":yields {typehint}: ",
                    description,
                ),
            )
        else:
            lines.extend(
                self._format_block(
                    ":yields: ",
                    description,
                ),
            )

    if lines and lines[-1]:
        lines.append("")

    return lines


